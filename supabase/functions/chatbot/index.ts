import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are a helpful assistant for ContractGuard, an AI-powered contract analysis platform. Your role is to:

1. **Help users understand the platform:**
   - Explain how to upload and analyze contracts
   - Describe the risk scoring system (0-10 scale)
   - Guide users through the analysis results

2. **Provide contract guidance:**
   - Explain common contract terms and clauses
   - Help users understand what to look for in different contract types (employment, rental, NDA, etc.)
   - Provide general tips for negotiating better terms

3. **Answer FAQs:**
   - What types of contracts can be analyzed (employment, rental, service, NDA, sales, partnership)
   - How the risk assessment works
   - How to interpret flagged clauses
   - Privacy and security of uploaded documents

**Important guidelines:**
- Be concise and helpful
- Use plain language, avoid legal jargon when possible
- Always remind users that this is not legal advice when discussing specific contract terms
- If users ask about specific legal situations, recommend consulting a licensed attorney
- Be friendly and professional

**Key features to explain:**
- Contract upload and analysis
- Risk score visualization (0-10 scale)
- Clause-by-clause breakdown
- Plain-language explanations
- Negotiation suggestions
- Analysis history`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");

    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages.map((m: { role: string; content: string }) => ({
            role: m.role,
            content: m.content,
          })),
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Service temporarily unavailable." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chatbot error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
